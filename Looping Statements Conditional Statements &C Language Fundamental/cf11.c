//11.Find circumference of square formula : C = 4 * a
#include <stdio.h>

int main() {
    float side, perimeter;

    printf("Enter the length of a side of the square: ");
    scanf("%f", &side);

    perimeter = 4 * side;

    printf("The perimeter of the square is: %.2f units\n", perimeter);

        
}

